#include "../includes/libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	int i;

	i = 0;
	i = ft_lstsize(lst) - 1;
	while (i > 0)
	{
		i--;
		lst = lst->next;
	}
	return (lst);
}
